'''Crie um programa com uma função que necessite de três parâmetros e então que retorne a sua soma.'''

def soma_tres (numeroA, numeroB, numeroC):
    soma_tres = numeroA+numeroB+numeroC
    return soma_tres

numeroA = int(input("Digite o primeiro número: "))
numeroB = int(input("Digite o segudo número: "))
numeroC = int(input("Digite o terceiro número: "))
print (numeroA+numeroB+numeroC)
