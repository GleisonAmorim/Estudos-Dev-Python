numero1 = 10
numero2 =  2 
''' + - * / '''

print(f'A soma (+) dos números {numero1} e {numero2} é :', numero1 + numero2)
print(f'A subtração (-) dos números {numero1} e {numero2} é :', numero1 - numero2)
print(f'A multiplicação (*) dos números {numero1} e {numero2} é :', numero1 * numero2)
print(f'A divisão (/) dos números {numero1} e {numero2} é   :', numero1 / numero2)
print(f'O resto da divisão (%) dos números {numero1} e {numero2} é   :', numero1 % numero2)